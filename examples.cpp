
#include <iostream>
#include <stdlib.h>

using namespace std;

////////////////////////////////////////////////////////////////////////////////////

int f(int* a) {
    return (*a) * 2;
}

void null_pointer1() {
    int* p = NULL;
    cout << f(p) << endl;
}
////////////////////////////////////////////////////////////////////////////////////

bool null_pointer2(int* v) {
    return (v == NULL && *v == 3);
}
////////////////////////////////////////////////////////////////////////////////////

void null_pointer_array(int *p) {
    if (p)
        return;
    
    int x = p[0];
    cout << x << endl;
}
////////////////////////////////////////////////////////////////////////////////////

void malloc_delete_calls() {
    int *p = (int *)malloc(sizeof(int));
    delete p;
}

////////////////////////////////////////////////////////////////////////////////////

int main(int argc, const char * argv[]) {
    null_pointer1();
    null_pointer2(NULL);
    null_pointer_array(NULL);
    malloc_delete_calls();
    return 0;
}
